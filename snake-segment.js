class SnakeSegment {
  constructor(segmentX, segmentY, width, height, segmentColor, head, direction) {
    this.segmentX = segmentX;
    this.segmentY = segmentY;
    this.width = width;
    this.height = height;
    this.segmentColor = segmentColor;
    this.head = head;
    this.direction = direction;
  }
}